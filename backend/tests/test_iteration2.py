# proper 32x32 PNG generated by Pillow (so reportlab can read it for PDF embedding)
def _make_png():
    from PIL import Image
    img = Image.new("RGB", (32, 32), (24, 95, 165))
    buf = io.BytesIO()
    img.save(buf, format="PNG")
    return buf.getvalue()

PNG_BYTES = _make_png()


